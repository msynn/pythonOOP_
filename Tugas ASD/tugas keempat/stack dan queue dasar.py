# Konsep dasar
tumpukan = [1,2,3]
print("Ini adalah stack awal :",tumpukan)

print("\n")
# Menambahkan 
tumpukan.append(4)
print("Ketika pertama kali ditambahkan :", tumpukan)

print("\n")
tumpukan.append(5)
print("Ketika kedua kali ditambahkan :", tumpukan)

print("\n")
# Meghapus atau pop di stack
tumpukan.pop()
print("Ini ketika sudah di pop pertama kali : ", tumpukan)

print("\n")
tumpukan.pop()
print("Ini ketika sudah di pop kedua kali : ", tumpukan)


